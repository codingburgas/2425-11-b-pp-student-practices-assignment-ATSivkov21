import os

class Config:
    SECRET_KEY = os.environ.get('SECRET_KEY') or 'dev-secret-key'
    SQLALCHEMY_DATABASE_URI = os.environ.get('DATABASE_URL') or 'sqlite:///site.db'
    SQLALCHEMY_TRACK_MODIFICATIONS = False
    
    # Email Configuration
    MAIL_SERVER = os.environ.get('MAIL_SERVER') or 'smtp.gmail.com'
    MAIL_PORT = int(os.environ.get('MAIL_PORT') or 587)
    MAIL_USE_TLS = os.environ.get('MAIL_USE_TLS', 'true').lower() in ['true', 'on', '1']
    MAIL_USE_SSL = os.environ.get('MAIL_USE_SSL', 'false').lower() in ['true', 'on', '1']
    MAIL_USERNAME = os.environ.get('EMAIL_USER') or os.environ.get('MAIL_USERNAME')
    MAIL_PASSWORD = os.environ.get('EMAIL_PASS') or os.environ.get('MAIL_PASSWORD')
    MAIL_DEFAULT_SENDER = os.environ.get('MAIL_DEFAULT_SENDER') or 'noreply@example.com'
    
    # Disable email confirmation if no email credentials are provided
    EMAIL_CONFIRMATION_ENABLED = bool(MAIL_USERNAME and MAIL_PASSWORD)

class DevelopmentConfig(Config):
    DEBUG = True
    # For development, you can disable email confirmation entirely
    EMAIL_CONFIRMATION_ENABLED = False

class ProductionConfig(Config):
    DEBUG = False